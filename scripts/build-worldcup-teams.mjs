// One-time data build: FIFA-22 players CSV -> committed national-team squads.
//
// Usage:
//   node scripts/build-worldcup-teams.mjs [path/to/players_22.csv]
//
// Reads the FIFA dataset, groups players by nation, keeps a position-balanced
// pool of the best players per nation, and emits lib/worldcup/teams.generated.ts.
// The runtime never touches the CSV — only the generated TS file.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DEFAULT_CSV = "/Users/Aplay/Desktop/tcg 5ive/fifa players/players_22.csv";
const csvPath = process.argv[2] || DEFAULT_CSV;
const OUT = path.join(__dirname, "..", "lib", "worldcup", "teams.generated.ts");

const N_NATIONS = 47; // + the user's card team = 48 entrants
// Position-balanced pool kept per nation (covers every formation + rotation):
const POOL = { GK: 2, DEF: 5, MID: 5, ATT: 4 };

// ── minimal RFC-4180 CSV parser (handles quoted fields, commas, newlines) ──────
function parseCsv(text) {
  const rows = [];
  let row = [], field = "", inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') { field += '"'; i++; }
        else inQuotes = false;
      } else field += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ",") { row.push(field); field = ""; }
    else if (c === "\n") { row.push(field); rows.push(row); row = []; field = ""; }
    else if (c === "\r") { /* skip */ }
    else field += c;
  }
  if (field.length || row.length) { row.push(field); rows.push(row); }
  return rows;
}

function mapPos(positions) {
  const primary = (positions || "").split(",")[0].trim().toUpperCase();
  if (primary === "GK") return "GK";
  if (["CB", "LB", "RB", "LWB", "RWB", "LCB", "RCB"].includes(primary)) return "DEF";
  if (["CDM", "CM", "CAM", "LM", "RM", "LDM", "RDM", "LCM", "RCM", "LAM", "RAM", "DM"].includes(primary)) return "MID";
  if (["ST", "CF", "LW", "RW", "LF", "RF", "LS", "RS"].includes(primary)) return "ATT";
  return "MID"; // safe default
}

function slug(s) {
  return s.toLowerCase().normalize("NFD").replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

console.log("Reading", csvPath);
const text = fs.readFileSync(csvPath, "utf8");
const rows = parseCsv(text);
const header = rows[0];
const idx = (name) => header.indexOf(name);
const I = {
  id: idx("sofifa_id"), name: idx("short_name"), positions: idx("player_positions"),
  overall: idx("overall"), nation: idx("nationality_name"),
  face: idx("player_face_url"), flag: idx("nation_flag_url"),
};

const byNation = new Map();
for (let r = 1; r < rows.length; r++) {
  const row = rows[r];
  if (!row || row.length < header.length) continue;
  const overall = parseInt(row[I.overall], 10);
  const nation = row[I.nation];
  if (!Number.isFinite(overall) || !nation) continue;
  if (!byNation.has(nation)) byNation.set(nation, { flag: row[I.flag], players: [] });
  byNation.get(nation).players.push({
    id: row[I.id],
    name: row[I.name],
    overall,
    pos: mapPos(row[I.positions]),
    faceUrl: row[I.face] || "",
  });
}

function buildSquad(nation, data) {
  const sorted = [...data.players].sort((a, b) => b.overall - a.overall);
  const byPos = { GK: [], DEF: [], MID: [], ATT: [] };
  for (const p of sorted) byPos[p.pos].push(p);

  // position-balanced pool
  const picked = [];
  const seen = new Set();
  for (const pos of ["GK", "DEF", "MID", "ATT"]) {
    for (const p of byPos[pos].slice(0, POOL[pos])) { picked.push(p); seen.add(p.id); }
  }
  // top-up with best remaining so the pool is robust
  for (const p of sorted) {
    if (picked.length >= 14) break;
    if (!seen.has(p.id)) { picked.push(p); seen.add(p.id); }
  }

  const gk = byPos.GK[0];
  const outfield = sorted.filter((p) => p.pos !== "GK").slice(0, 6);
  if (!gk || outfield.length < 6) return null; // not enough for 7-a-side

  const best7 = [gk, ...outfield];
  const squadOverall = Math.round(best7.reduce((s, p) => s + p.overall, 0) / best7.length);

  return {
    id: slug(nation),
    name: nation,
    flagUrl: data.flag || "",
    squadOverall,
    players: picked,
  };
}

const squads = [];
for (const [nation, data] of byNation) {
  const sq = buildSquad(nation, data);
  if (sq) squads.push(sq);
}
squads.sort((a, b) => b.squadOverall - a.squadOverall);
const top = squads.slice(0, N_NATIONS);

const banner = `// AUTO-GENERATED by scripts/build-worldcup-teams.mjs — do not edit by hand.
// Source: FIFA-22 dataset. ${top.length} national squads.
`;
const body = `import type { WcNation } from "./types";

export const WC_NATIONS: WcNation[] = ${JSON.stringify(top, null, 2)};
`;
fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, banner + "\n" + body);
console.log(`Wrote ${top.length} nations -> ${path.relative(process.cwd(), OUT)}`);
console.log("Strongest:", top.slice(0, 5).map((t) => `${t.name} ${t.squadOverall}`).join(", "));
console.log("Weakest:", top.slice(-5).map((t) => `${t.name} ${t.squadOverall}`).join(", "));
